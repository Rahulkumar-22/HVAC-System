    private void temperatureKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_temperatureKeyPressed

        // TODO add your handling code here:
        jLabel7.setForeground(Color.RED);
        if(temperature.getText().length()==0)
        {
            jLabel7.setText(" ");
        }
        else{
        try{
            double d=Double.parseDouble(temperature.getText());
            jLabel7.setText(" ");
        }catch(NumberFormatException e)
        {
            jLabel7.setText("Invalid Input");
        }
        }
    }//GEN-LAST:event_temperatureKeyPressed

